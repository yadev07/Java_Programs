//Write a program that demonstrates explicit type casting by converting a double to an int and printing both values.
class Q29 {
	public static void main(String[] args) {
		double val1 = 8.73 ; 
		
		int val2 = (int) val1 ;
		
		System.out.println("Double value: " + val1);
		System.out.println("Int value: " + val2);
		
	}
}